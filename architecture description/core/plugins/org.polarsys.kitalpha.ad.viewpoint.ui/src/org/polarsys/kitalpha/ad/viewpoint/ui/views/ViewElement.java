package org.polarsys.kitalpha.ad.viewpoint.ui.views;

/**
 * @author Guillaume Gebhart
 *
 */
public class ViewElement {

	private String id;
	private String name;
	/**
	 * Constructor
	 * @param id
	 * @param name
	 */
	public ViewElement(final String id, final String name) {
		super();
		this.id = id;
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	
}
